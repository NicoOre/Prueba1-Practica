    private void autoridadBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_autoridadBtnActionPerformed

        int dia = Integer.parseInt(diaTxt.getText());
        int mes = Integer.parseInt(mesTxt.getText()) - 1;
        int anio = Integer.parseInt(anioTxt.getText()) - 1900;
        Date d = new Date(anio, mes, dia);
        m.agregarAutoridad(new Persona(nombreTxt.getText(), apellidoTxt.getText(), cedulaTxt.getText(), direccionTxt.getText(), generoTxt.getText(), d));
        
    }//GEN-LAST:event_autoridadBtnActionPerformed

